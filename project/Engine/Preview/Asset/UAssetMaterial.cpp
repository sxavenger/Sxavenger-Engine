#include "UAssetMaterial.h"

////////////////////////////////////////////////////////////////////////////////////////////
// Albedo structure
////////////////////////////////////////////////////////////////////////////////////////////

void UMaterial::Albedo::Init() {
	type  = Type::Value;

	color = kWhite3<float>;
	index = NULL;
}

////////////////////////////////////////////////////////////////////////////////////////////
// Transparency structure methods
////////////////////////////////////////////////////////////////////////////////////////////

void UMaterial::Transparency::Init() {
	type  = Type::Value;

	alpha = 1.0f;
	index = NULL;
}

////////////////////////////////////////////////////////////////////////////////////////////
// Normal structure methods
////////////////////////////////////////////////////////////////////////////////////////////

void UMaterial::Normal::Init() {
	type  = Type::None;

	index = NULL;
}

////////////////////////////////////////////////////////////////////////////////////////////
// Property structure methods
////////////////////////////////////////////////////////////////////////////////////////////

void UMaterial::Property::Init() {
	type  = Type::Value;

	value = 0.0f;
	index = NULL;
}

////////////////////////////////////////////////////////////////////////////////////////////
// SurfaceProperties structure methods
////////////////////////////////////////////////////////////////////////////////////////////

void UMaterial::SurfaceProperties::Init() {
	ao.Init();
	roughness.Init();
	metallic.Init();
}

////////////////////////////////////////////////////////////////////////////////////////////
// UAssetMaterial class methods
////////////////////////////////////////////////////////////////////////////////////////////

json UAssetMaterial::Serialize() const {
	json data = UBaseAsset::Serialize();
	return data;
}
