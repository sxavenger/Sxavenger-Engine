#include "WinApp.h"

////////////////////////////////////////////////////////////////////////////////////////////
// WinApp class methods
////////////////////////////////////////////////////////////////////////////////////////////

void WinApp::Init() {
	CoInitializeEx(0, COINIT_MULTITHREADED);
	timeBeginPeriod(1);

	MFStartup(MF_VERSION, MFSTARTUP_NOSOCKET);
}

void WinApp::Term() {
	MFShutdown();
	
	CoUninitialize();
}
